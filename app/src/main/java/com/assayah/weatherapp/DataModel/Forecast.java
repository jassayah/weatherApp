package com.assayah.weatherapp.DataModel;

public class Forecast {

    private int code;
    private String date;
    private String day;
    private int high;
    private int low;
    private String text;


    public int getCode() {
        return code;
    }

    public String getDate() {
        return date;
    }

    public String getDay() {
        return day;
    }

    public int getHigh() {
        return high;
    }

    public int getLow() {
        return low;
    }

    public String getText() {
        return text;
    }

}
